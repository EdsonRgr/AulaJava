package view2;

public class OperacoesMat {

public double soma (double valor1 , double valor2) {
		return valor1+valor2;
}
public double mult (int mult1 , int mult2) {
	return mult1*mult2;
}
public double div (int div1 , int div2) {
	return div1/div2;
}
public double sub (int sub1 , int sub2) {
	return sub1-sub2;
}


}